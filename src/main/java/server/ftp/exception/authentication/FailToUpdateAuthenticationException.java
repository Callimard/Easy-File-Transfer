package server.ftp.exception.authentication;

public class FailToUpdateAuthenticationException extends AuthenticationException {

	public FailToUpdateAuthenticationException() {
	}

	public FailToUpdateAuthenticationException(String arg0) {
		super(arg0);
	}

	public FailToUpdateAuthenticationException(Throwable arg0) {
		super(arg0);
	}

	public FailToUpdateAuthenticationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public FailToUpdateAuthenticationException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
