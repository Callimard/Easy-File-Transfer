package server.util.easy_server.exception.command_reader;

public class CommandExtractorException extends Exception {

	public CommandExtractorException() {
	}

	public CommandExtractorException(String arg0) {
		super(arg0);
	}

	public CommandExtractorException(Throwable arg0) {
		super(arg0);
	}

	public CommandExtractorException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public CommandExtractorException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
