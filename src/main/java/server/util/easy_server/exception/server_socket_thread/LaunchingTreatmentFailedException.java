package server.util.easy_server.exception.server_socket_thread;

public class LaunchingTreatmentFailedException extends ServerSocketThreadException {

	public LaunchingTreatmentFailedException() {
	}

	public LaunchingTreatmentFailedException(String arg0) {
		super(arg0);
	}

	public LaunchingTreatmentFailedException(Throwable arg0) {
		super(arg0);
	}

	public LaunchingTreatmentFailedException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public LaunchingTreatmentFailedException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
