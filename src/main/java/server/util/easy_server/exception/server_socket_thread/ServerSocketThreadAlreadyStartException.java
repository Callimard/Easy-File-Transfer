package server.util.easy_server.exception.server_socket_thread;

public class ServerSocketThreadAlreadyStartException extends ServerSocketThreadException {

	public ServerSocketThreadAlreadyStartException() {
	}

	public ServerSocketThreadAlreadyStartException(String arg0) {
		super(arg0);
	}

	public ServerSocketThreadAlreadyStartException(Throwable arg0) {
		super(arg0);
	}

	public ServerSocketThreadAlreadyStartException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ServerSocketThreadAlreadyStartException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
