package server.util.easy_server.exception.server_socket_thread;

public class ServerSocketThreadException extends Exception {

	public ServerSocketThreadException() {
	}

	public ServerSocketThreadException(String arg0) {
		super(arg0);
	}

	public ServerSocketThreadException(Throwable arg0) {
		super(arg0);
	}

	public ServerSocketThreadException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ServerSocketThreadException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
