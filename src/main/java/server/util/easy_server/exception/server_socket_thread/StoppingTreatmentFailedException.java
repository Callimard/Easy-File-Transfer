package server.util.easy_server.exception.server_socket_thread;

public class StoppingTreatmentFailedException extends ServerSocketThreadException {

	public StoppingTreatmentFailedException() {
	}

	public StoppingTreatmentFailedException(String arg0) {
		super(arg0);
	}

	public StoppingTreatmentFailedException(Throwable arg0) {
		super(arg0);
	}

	public StoppingTreatmentFailedException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public StoppingTreatmentFailedException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
